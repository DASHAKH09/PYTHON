s = 256
c = 80
n = 60

print(f'Электронная книга по программированию на английском языке состоит из {s} страниц.')
print(f'В среднем на странице {c} строк по {n} символов.')
print('Используется кодировка UTF-8. Определите объём текстового файла в Кбайтах.')
print()

print('Решение:')

r1 = s * c * n
print(f'1) {s} * {c} * {n} = {r1} - всего символов в книге')

r2 = r1 / 1024
print(f'2) {r1} / 1024 = {r2} - Кбайт')
print()

print(f'Ответ: {r2} Кбайт.')
